// Define the HelloWorld class
public class HelloWorld {
    
    // The main method is the entry point of a Java application
    public static void main(String[] args) {
        
        // Print "Hello, World!" to the console
        System.out.println("Hello, World!");
        
        // Print personal details
        System.out.println("My name is Vikas Chaudhary.");
        System.out.println("My favorite food is samosa; it's an Indian dish.");
        System.out.println("A celebrity I would like to meet: Justin Bieber.");
        System.out.println("My least favorite chore at home is washing dishes.");
        System.out.println("I would rather be sleeping right now.");
    }
}

/*
 * What does the main() method represent in a Java program?
 * - The main() method is the entry point of a Java application. It is where execution begins.
 *   The JVM (Java Virtual Machine) calls this method to start the program.
 *
 * Why is System.out.println() used to display output?
 * - System.out.println() is used to print text to the console. "System.out" refers to the standard output stream,
 *   and "println()" prints the text followed by a new line.
 *
 * What is the purpose of comments in the program?
 * - Comments are used to explain code, making it easier to read and understand.
 *   They are ignored by the compiler and do not affect program execution.
 */